            Este magnífico monumento es uno de los símbolos más reconocidos de la ciudad. Es una de las antiguas puertas que tenía la ciudad y que fue inaugurada en 1791 como símbolo del triunfo de la paz frente al uso de la armas para resolver los conflictos. Con sus 26 metros de alto y su estilo neoclásico, su envergadura nos recuerda a una las grandes construcciones de la Antigua Grecia. En lo más alto del monumento podemos ver una cuadriga de bronce que representa 
