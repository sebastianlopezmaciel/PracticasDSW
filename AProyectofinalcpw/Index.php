        <p>La historia de Berlín, actual capital de Alemania, engloba el período comprendido desde su primera colonización humana en la prehistoria hasta la actualidad. En 1307 Berlín y Cölln (en la actual isla de los museos) se unieron en una ciudad que conservó el nombre de Berlín. En 1415 fue elegida capital de Brandeburgo y posteriormente ha sido capital del Reino de Prusia, del Imperio alemán, de la República de Weimar y de la Alemania nazi.5</p>

<p>Durante los últimos meses de la Segunda Guerra Mundial, los aliados bombardearon la mayoría de las ciudades alemanas. La Batalla de Berlín fue el enfrentamiento final entre Alemania y la Unión de Repúblicas Socialistas Soviéticas (URSS), que trajo como consecuencia directa la rendición incondicional de Alemania ante el Ejército Rojo.6</p>

<p>Tras el fin de la Segunda Guerra Mundial en Europa, Berlín fue ocupada militarmente en cuatro zonas. En 1948, en los tres sectores occidentales (Berlín Oeste), los Aliados del Oeste establecen el dinero de la República Federal de Alemania, RFA, tras la reforma monetaria. La Unión Soviética replica con el bloqueo de Berlín oeste y la creación de la República Democrática Alemana (RDA en 1949). El bloqueo de Berlín se implantó cuando los accesos por tierra a la parte occidental de la ciudad fueron bloqueados en 1948 por el Ejército Rojo. El bloqueo duró casi un año, y además de las tensiones políticas que provocó entre las potencias mundiales, es recordado por la laboriosa estrategia de aprovisionamiento de la población berlinesa occidental, a la cual abastecieron los ejércitos aliados con un «puente aéreo» a lo largo de varios meses.7</p>

<p>En 1961, la RDA construyó el muro de Berlín para separar las dos partes de la ciudad, y de hecho aislar Berlín Oeste de la RDA. La caída del Muro de Berlín se dio en 1989 al aceptar, el gobierno de la RDA, la libre circulación de los ciudadanos entre las dos partes de la ciudad. Al año siguiente con la Reunificación alemana desaparece la RDA, anexionada de hecho en la RFA, que traslada su capital de Bonn a Berlín en 1990, dando con ello ingreso en la Unión Europea (UE) a la población de la desaparecida república.</p>
